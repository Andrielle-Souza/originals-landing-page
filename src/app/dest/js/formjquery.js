/* --------------------------------- submit --------------------------------- */
function enableSuccess() {
    $('.form-content').addClass('contentHidden')
    this.setTimeout(() => {
        $('.success').addClass('tksEnabled')
        $('.form-content').addClass('contentDisabled')
    }, 250)
}

$('.btn-form').on('click', () => {
    return enableSuccess()
})

